<div id="amperemeter" class="helpmenu">
<h2><legend>Ampere meter</legend></h2>
<p><strong>Ampere meter</strong> adalah alat untuk mengukur Kuat Arus Listrik atau Intensitas listrik (I). Kuat arus listrik dihitung dalam satuan Ampere (A). Ampere meter juga tersedia untuk berbagai tingkatan besaran arus, mulai dari yang sangat kecil, micro Ampere (&micro;A), milli Ampere (mA) dan Ampere (A). Satuan yang lebih besar, misalnya kilo Ampere (kA) jarang ditemui di pasaran karena pemakaiannya terbatas, hanya pada tempat-tempat yang mengelola konsumsi arus besar, misalnya di pabrik peleburan baja atau pusat-pusat beban listrik.</p> 
<p>Tidak setiap pengukuran arus yang besar memerlukan Ampere meter dengan batas ukur yang besar pula. Untuk beberapa kondisi, batas ukur Ampere meter dapat ditingkatkan dengan membuat rangkaian perluasan batas ukur pada Ampere meter yang sudah ada.</p>
<p class="text-center">
<img src="equ_images/Plat Ukur/50ua50.png" alt="Ampere meter -50uA 50uA" width="160" />
<img src="equ_images/Plat Ukur/500ma.png" alt="Ampere meter 500mA" width="160" />
<img src="equ_images/Plat Ukur/10a.png" alt="Ampere meter 10A" width="160" />
<div class="text-center">Gambar: Skala Ukur Ampere-meter</div>
</p>
<p>Dalam rangkaian listrik yang diukur, Ampere meter dihubungkan secara serie. Untuk rangkaian arus searah atau <em>direct current</em> (DC) polaritas harus diperhatikan, tidak boleh terbalik. Penyambungan secara serie artinya adalah menyambungkan terminal Ampere meter yang bernotasi plus (+) dengan bagian yang berpolaritas minus (-) dalam rangkaian listrik dan terminal minus (-) dengan bagian yang berpolaritas plus (+) pada rangkaian listrik. Sehingga Ampere meter menjadi bagian penyalur arus dalam rangkaian.</p><p>Dalam rangkaian arus bolak-balik atau <em>alternating current</em> (AC) Ampere meter juga dihubungkan secara serie namun tidak perlu memperhatikan polaritas. Ampere meter AC dapat dihubungkan terbolak-balik.</p>
<div class="text-center"><img src="images/instalasi_amperemeter_AC.png" height="220px" />&nbsp;&nbsp;<img src="images/instalasi_amperemeter_DC.png" height="220px" /></div>
<div class="text-center">Gambar: Cara penyambungan Ampere-meter AC dan DC</div>
</div>

<div id="wattmeter" class="helpmenu">
<h2><legend>Watt meter</legend></h2>
<p><strong>Watt meter</strong> adalah alat ukur daya listrik, dinyatakan dalam satuan Watt (W). Watt meter dalam skala ukur besar banyak ditemukan di pasaran misalnya dalam satuan kilo Watt (kW) atau bahkan Mega Watt (MW). Watt meter tersedia dalam jenis Watt meter DC atau AC, namun yang paling banyak adalah Watt meter AC.</p>
<p class="text-center">
<img src="equ_images/Plat Ukur/500w.png" alt="Watt meter 500W" width="160" />
<img src="equ_images/Plat Ukur/kw3.png" alt="kilo Watt meter 3kW" width="160" />
<div class="text-center">Gambar: Skala Ukur Watt-meter</div>
</p>
<p>Watt meter mengukur Tegangan (V) x Kuat Arus (I), oleh karena itu dalam Watt meter terdapat kumparan Tegangan (L<sub>V</sub>) dan Kumparan Arus (L<sub>I</sub>) yang harus dihubungkan dengan benar sebagaimana menghubungkan Volt meter dan Ampere meter. Karena itu Watt meter cara menghubungkannya dengan beban listrik yang diukur adalah secara Serie-Paralel.</p>
<div class="text-center"><img src="images/instalasi_wattmeter_AC.png" height="220px" /></div>
<div class="text-center">Gambar: Cara penyambungan Watt-meter AC</div>
</div>

<div id="freqmeter" class="helpmenu">
<h2><legend>Frequency meter</legend></h2>
<p><strong>Frequency meter</strong> digunakan untuk mengukur getaran. Dalam sistem kelistrikan Frequency meter digunakan untuk mengukur frekuensi sumber listrik yang dinyatakan dalam satuan Hertz (Hz). Secara teknis, Frekuensi meter bekerja dengan menghitung jumlah gelombang sinus yang dihasilkan oleh sumber tegangan arus bolak-balik (Alternator) dalam setiap detik. Untuk pemakaian dalam sistem kelistrikan Frekuensi meter tersedia dalam batas ukur hingga 60 Hertz. Batas ukur yang lebih tinggi biasanya diperlukan dalam bidang elektronika.</p>
<p class="text-center">
<img src="equ_images/Plat Ukur/hz.png" alt="Watt meter 500W" width="160" />
<div class="text-center">Gambar: Skala Ukur Frequency-meter</div>
</p>
<p>Frequency meter dihubungkan dalam sistem instalasi secara paralel, seperti menghubungkan Volt-meter AC, tidak memperhatikan polaritas.</p>
<div class="text-center"><img src="images/instalasi_freqmeter.png" height="220px" /></div>
<div class="text-center">Gambar: Cara penyambungan Frequency-meter</div>
