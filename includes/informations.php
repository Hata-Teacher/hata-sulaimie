<h2><legend>Volt meter</legend></h2>
<p>Volt meter digunakan untuk mengukur tegangan atau beda potensian listrik antara dua titik dalam sebuah rangkaian listrik.</p>
<p><strong>Volt meter</strong> dibuat dalam berbagai tingkatan kemampuan pengukuran. Untuk Beda potensial yang umum dapat menggunakan Volt-meter dalam satuan 'Volt' (V). Untuk pengukuran beda potensial yang lebih besar tersedia Volt meter dalam satuan 'kilo Volt' (kV). Sedangkan untuk mengukur beda potensial yang lebih kecil dapat menggunakan 'milli Volt' meter (mV) atau 'micro Volt' meter (&micro;V) untuk beda potensial yang sangat kecil.</p>
<p class="text-center">
<img src="equ_images/Plat Ukur/50v.png" alt="Volt meter 50V" width="160" />
<img src="equ_images/Plat Ukur/150v.png" alt="Volt meter 150V" width="160" />
<img src="equ_images/Plat Ukur/100v100.png" alt="Volt meter -100V tp 100V" width="160" />
<div class="text-center">Gambar: Skala Ukur Volt-meter</div>
</p>
<p>Volt meter dihubungkan dengan titik-titik pengukuran secara paralel dengan memperhatikan polaritas, artinya kutub plus (+) atau minus (-) tidak boleh terbalik. Untuk kondisi dimana polaritas tegangan dapat terbolak-balik tersedia Volt meter dengan penunjukan dan skala ukur khusus.</p>
