<h2><legend>Rencana Pengembangan</legend></h2>
<p>Dalam aspek metode pembelajaran, aplikasi ini akan dilengkapi dengan mode <strong>Quiz</strong>. Mode quiz adalah fitur permainan sambil belajar, dimana Anda dapat membuat suatu persoalan mengenai belajar membaca alat ukur, baik dalam mode <strong>Meter to Value</strong> atau mode <strong>Value to Meter</strong>.</p>
