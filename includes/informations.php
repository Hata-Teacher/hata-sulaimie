<div id="teknis" class="helpmenu">
<h2><legend>Fungsi Aplikasi</legend></h2>
<p>Sebagai alat bantu belajar <strong>Membaca Alat Ukur Listrik</strong>, aplikasi ini dirancang untuk dapat digunakan dalam 2 mode, yaitu: 
<ul>
  <li><strong>Membaca Penunjukan</strong> : Jarum ukur digerakkan menuju suatu titik tertentu yang diinginkan dan aplikasi akan menunjukkan hasil ukurnya (value). Melalui mode ini anda akan berlatih membaca skala alat ukur dan selanjutnya dapat melihat nilai pengukuran yang seharusnya. Sebagai variasi pembelajaran, hasil ukur dapat disembunyikan terlebih dahulu dan akan dibuka kemudian setelah anda menebak hasil ukurnya.</li>
  <li><strong>Menentukan Posisi Jarum</strong> : Nilai pengukuran ditentukan terlebih dahulu dan jarum ukur akan bergerak menuju titik ukur yang sesuai. Sebelum menunjukkan gerakan jarum ukur, anda dapat menentukan atau menduga di mana jarum ukur akan menunjuk.</li>
</ul>
</p>
<p>Sebagai tambahan pembelajaran, di samping belajar membaca alat ukur, aplikasi ini juga dilengkapi dengan penjelasan tekstual dan ilustrasi cara penggunaan alat ukur yang sedang anda gunakan, melalui menu kontekstual <strong>Instalasi</strong>.</p>
</div>

<!------- LATAR BELAKANG -------------------------------------------------------->
<div id="background">
<h2><legend>Latar Belakang</legend></h2>
<p>Membaca Alat ukur analog secara umum dapat dikatakan lebih sulit karena alat ukur tidak langsung menunjukkan nilai sebagaimana pada alat ukur digital. Pada alat ukur analog, orang yang melakukan pengukuran harus menafsirkan sendiri hasil pengukurannya. Oleh karena itu diperlukan pemahaman yang baik terhadap cara membaca skala dari alat ukur yang digunakannya. Secara pribadi orang yang memiliki preferensi terhadap kelistrikan atau elektronika sudah seharusnya memiliki pemahaman yang baik terhadap penggunaan alat ukur analog.</p>
</div>

