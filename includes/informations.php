<h2><legend>Menggerakkan Jarum Ukur</legend></h2>
<p>Jarum ukur dapat digerakkan menggunakan Keyboard maupun Mouse, sebagaimana berikut ini:</p>
<p><strong>Menggerakkan Jarum menggunakan Keyboard</strong><br />
<kbd><i class="fa fa-arrow-right"></i></kbd> jarum bergerak ke kanan 1 divisi<br />
<kbd><i class="fa fa-arrow-left"></i></kbd> jarum bergerak ke kiri 1 divisi<br />
<div class="info">Lebar setiap divisi tergantung pada Skala Ukur. Oleh karena itu lebar langkah pergerakan jarum pada setiap alat ukur dapat berbeda-beda.</div></p>
<p><strong>Perlambatan Gerak</strong><br />
Langkah Pergerakan jarum dapat diperlambat dengan menekan tombol <kbd>Ctrl</kbd>, <kbd>Shift</kbd>, <kbd>Alt</kbd> atau kombinasinya bersama tombol <kbd><i class="fa fa-arrow-left"></i></kbd> <kbd><i class="fa fa-arrow-right"></i></kbd>.</p>
<p id="perlambatan">
Tingkat perlambatan gerak:<br />
<table>
<tr><td><kbd>Ctrl</kbd></td><td> langkah gerak &frac12; divisi</td></tr>
<tr><td><kbd>Shift</kbd></td><td> langkah gerak &frac14; divisi</td></tr>
