<p>Untuk meningkatkan fungsi aplikasi ini sebagai alat bantu belajar, ke depan akan dilengkapi dengan quiz (soal dan penilaian). Soal Quiz dapat ditujukan kepada seseorang tertentu yang akan diberikan penilaian. Item Quiz dapat dibuat secara otomatis oleh aplikasi maupun soal/kesulitaan yang dibuat oleh siswa yang lain.</p>
