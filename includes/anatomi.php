<!--- ZERO ADJUST -------------------------------------------------->
<div class="helpmenu axe_knob">
<h2><legend>ZERO ADJUST SCREW</legend></h2>
<p class="text-center">
<img src="images/axe6.png" />
</p>
<ol>
  <li><strong>Fungsi</strong> mengkalibrasi jarum ukur pada posisi NOL ketika belum digunakan.</li>
  <li><strong><em>Zero Adjust Screw</em></strong> menyesuaikan posisi sandaran jarum ukur secara mekanis. Oleh sebab itu bagian ini sebaiknya tidak sering diubah karena akan berpengaruh pada akurasi pengukuran.</li>
</ol>
<ul class="keterangan" style="background-color: #FFFF80;">
   <li><strong>Kalibrasi</strong> adalah proses membandingkan penunjukan alat ukur dengan penunjukan standar yang semestinya.</li>
   <li>Zero Adjust Screw ini juga disebut <em>Needle Corrector</em></li>
</ul>
</div>

<!--- ZERO OHM ADJUS -------------------------------------------------->
<div class="helpmenu adj_knob">
<h2><legend>ZERO OHM ADJUST KNOB</legend></h2>
<p class="text-center">
<img src="images/adjust6.png" />
</p>
<ol>
  <li><strong>Fungsi</strong> mengkalibrasi jarum ukur pada posisi NOL OHM pada saat kedua <em>probes</em> disatukan.</li>
  <li><strong><em>Zero Ohm Adjust Knob</em></strong> menyesuaikan posisi jarum ukur secara elektris dengan cara mengatur torsi simpangan jarum.</li>
</ol>
<ul class="keterangan" style="background-color: #FFFF80;">
  <li><strong>Kalibrasi Ohm meter</strong> perlu dilakukan ulang jika:
     <ul>
         <li>Energi battery sudah berkurang (karena penggunaan)</li>
         <li>Berganti faktor pengali (X1, X10, X100, dll).</li> 
     </ul>
  </li>
</ul>
</div>

<!--- SELEKTOR SWITCH -------------------------------------------------->
<div class="helpmenu sel_knob">
<h2><legend>SELECTOR SWITCH</legend></h2>
<p class="text-center">
<img src="images/range-selector.png" style="width: 250px;" />
</p>
<ol>
  <li><strong>Fungsi</strong> memilih mode multimeter beserta rentang atau batas ukurnya.</li>
  <li>Pada mode <strong>Ohm meter</strong> angka yang ditunjuk <strong><em>selector</em></strong> bukan batas ukur melainkan faktor pengali (<em>multiplier</em>).</li>
</ol>
</div>

<!--- SKALA -------------------------------------------------->
<div class="helpmenu skala">
<h2><legend>METER SCALE</legend></h2>
<p class="text-center">
<img src="images/icon.png" style="width: 280px;" />
</p>
<ol>
  <li><strong>Meter Scale</strong> atau <strong>Skala Ukur</strong> adalah papan yang berisi sejumlah skala pengukuran di mana penunjukan jarum ukur dibaca.</li>
  <li><strong>Cermin</strong> yang ada pada <em>Papan Skala Ukur</em> berfungsi untuk menghindari kesalahan pembacaan (paralax) dengan cara mensejajarkan pandangan terhadap jarum ukur dan bayangan pada cermin.</li>
</ol>
</div>

